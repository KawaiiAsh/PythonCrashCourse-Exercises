cars = ['bnw','audi','toyota','subaru']

# Sort
cars.sort()
print(cars)

# Reverse in sort
cars = ['bnw','audi','toyota','subaru']
cars.sort(reverse=True)
print(cars)

# Sorted
cars = ['bnw','audi','toyota','subaru']
print("Here is the original list")
print(cars)
print("Here is the sorted list")
print(sorted(cars))
print("Here is the original list again")
print(cars)

# Reverse
cars = ['bnw','audi','toyota','subaru']
print(cars)
cars.reverse()
print(cars)

# Lenght
print(len(cars))